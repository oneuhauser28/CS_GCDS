x = int(input('Enter number 1:'))
y = int(input('Enter number 2:'))
z = int(input('Enter number 3:'))

operation = input ('Enter an operation (+/-/*):')
if operation == '+':
    result = x + y + z
elif operation == "-":
    result = x - y - z
else:
    result = x * y * z

print (result)

     
    

